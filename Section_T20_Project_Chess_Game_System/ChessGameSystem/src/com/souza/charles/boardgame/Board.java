import java.io.Serializable;

public class Board implements Serializable {

    private int rows;
    private int columns;
    private Piece[][] pieces;


    public Board(int rows, int columns) {
        this.rows = rows;
        this.columns = columns;
        pieces = new Piece[rows][columns];
    }

    public int getRows() {
        return rows;
    }
}